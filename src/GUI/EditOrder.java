    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged

        if (evt.getStateChange() == ItemEvent.SELECTED) {
            Date d = new Date();

            SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd");
            String date = sdf.format(d);

            ResultSet rs = search("SELECT * FROM meal INNER JOIN ward WHERE meal.ward_master_id = ward.ward_id && meal.date = '" + date + "' && ward.ward = '" + evt.getItem().toString() + "'");

            try {
                if (rs.next()) {

                    ResultSet rs1 = search("SELECT * FROM meal_item WHERE meal_item.meal_master_id = '" + rs.getString("meal_id") + "'");

                    mealId = rs.getString("meal_id");

                    while (rs1.next()) {
                        if (rs1.getString("diet_type").equals("no_diet")) {
                            jTextField1.setText(rs1.getString("qty"));
                            diet_type.put("no_diet", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("1_diet")) {
                            jTextField3.setText(rs1.getString("qty"));
                            diet_type.put("1_diet", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("2_diet")) {
                            jTextField5.setText(rs1.getString("qty"));
                            diet_type.put("2_diet", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("3_diet")) {
                            jTextField4.setText(rs1.getString("qty"));
                            diet_type.put("3_diet", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("4_diet")) {
                            jTextField6.setText(rs1.getString("qty"));
                            diet_type.put("4_diet", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("3_ld")) {
                            jTextField8.setText(rs1.getString("qty"));
                            diet_type.put("3_ld", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("S1")) {
                            jTextField7.setText(rs1.getString("qty"));
                            diet_type.put("S1", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("S2")) {
                            jTextField9.setText(rs1.getString("qty"));
                            diet_type.put("S2", rs1.getString("meal_iteam_id"));

                        } else if (rs1.getString("diet_type").equals("S3")) {
                            jTextField10.setText(rs1.getString("qty"));
                            diet_type.put("S3", rs1.getString("meal_iteam_id"));

                        }

                    }

                    ResultSet rs2 = search("SELECT * FROM meal_extra_item INNER JOIN food WHERE meal_extra_item.food_master_id = food.food_id && meal_extra_item.meal_master_id = '" + rs.getString("meal_id") + "'");

                    TableModel model = jTable2.getModel();
                    DefaultTableModel dtm = (DefaultTableModel) model;
                    dtm.setRowCount(0);

                    int i = 1;
                    while (rs2.next()) {
                        Vector v = new Vector();

                        v.add("o_" + rs2.getString("meal_extra_item_id"));
                        v.add(rs2.getString("food"));
                        v.add(rs2.getString("qty"));

                        dtm.addRow(v);
                        i++;
                    }

                    jComboBox1.setEnabled(false);
                    jTextField1.requestFocus();

                } else {
                    JOptionPane.showMessageDialog(null, "This ward details are not in system. Please save details first", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                warning(ex);
            }
        }

    }//GEN-LAST:event_jComboBox1ItemStateChanged

