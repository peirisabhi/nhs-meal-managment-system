    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        TableModel model = jTable2.getModel();
        DefaultTableModel dtm = (DefaultTableModel) model;

        String food = jComboBox3.getSelectedItem().toString();
        int a = 0;

        for (int x = 0; x < jTable2.getRowCount(); x++) {

            if (food.equals(jTable2.getValueAt(x, 1).toString())) {
                int amount = Integer.parseInt(jTable2.getValueAt(x, 2).toString());
                int tot = amount + Integer.parseInt(jTextField2.getText());
                jTable2.setValueAt(tot, x, 2);
                a = 1;
            }

        }

        if (a == 0) {
            Vector<String> v = new Vector<>();

            v.add("n_" + String.valueOf(++i));
            v.add(food);
            v.add(jTextField2.getText());

            dtm.addRow(v);
        }

        jTextField2.setText("");
    }//GEN-LAST:event_jButton3ActionPerformed

